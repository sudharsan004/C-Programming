#include <stdio.h>
/*
Relational operators: >,<,>=,<=,==
*/

void main()
{
    int a = 10, b = 5;

    printf("\nGreater Than          : %d", a > b);
    printf("\nLesser Than           : %d", a < b);
    printf("\nGreater Than or Equal : %d", a >= b);
    printf("\nLesser Than or Equal  : %d", a <= b);
    printf("\nEqual to              : %d", a == b);
}