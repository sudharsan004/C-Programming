#include<stdio.h>
void main()
{
    int a =5;
    printf("\na=%d",a);
    printf("\nPre-Increment-%d",++a);
    printf("\na=%d",a);
    printf("\nPost-Increment-%d",a++);
    printf("\na=%d",a);
    printf("\nPre-Decrement-%d",--a);
    printf("\na=%d",a);
    printf("\nPost-Decrement-%d",a--);
    printf("\na=%d",a);
}